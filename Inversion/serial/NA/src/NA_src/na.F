c       Subroutine NNcalc_dlist - calculates square of distance from
c                                 all base points to new axis (defined
c                                 by dimension dim through point x.
c                                 It also updates the nearest node and
c                                 distance to the point x.
c
c       This is a full update of dlist, i.e. not using a previous dlist.
c
c-----------------------------------------------------------------------
c
        Subroutine NNcalc_dlist
     &             (dim,dlist,bp,nd,nb,x,nodex,dminx)
        real*4          bp(nd,*)
        real*4          x(nd)
        real*4          dlist(*)
        integer         dim

           dmin = 0.
           do j=1,dim-1
              d = (x(j)-bp(j,1))
              d = d*d
              dmin = dmin + d
           end do
           do j=dim+1,nd
              d = (x(j)-bp(j,1))
              d = d*d
              dmin = dmin + d
           end do
           dlist(1) = dmin
           d = (x(dim)-bp(dim,1))
           d = d*d
           dmin = dmin + d
           nodex = 1
c
           do i=2,nb
              dsum = 0.
              do j=1,dim-1
                 d = (x(j)-bp(j,i))
                 d = d*d
                 dsum = dsum + d
              end do
              do j=dim+1,nd
                 d = (x(j)-bp(j,i))
                 d = d*d
                 dsum = dsum + d
              end do
              dlist(i) = dsum
              d = (x(dim)-bp(dim,i))
              d = d*d
              dsum = dsum + d
              if(dmin.gt.dsum)then
                 dmin = dsum
                 nodex = i
              end if
              dnodex = dmin
           end do

c          write(*,*)' resetlist'
c          write(*,*)' dlist',(dlist(i),i=1,nb)
c          write(*,*)' input nodex',nodex

        return
        end


