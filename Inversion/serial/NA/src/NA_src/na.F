        Subroutine findnearest(smodelt,na_models,ntot,moddim,mopts)
        real            na_models(moddim,*)
        real            smodelt(*)

        common          /findnearestcom/dmin

        dmin = 0
        do i=1,moddim
          b = na_models(i,1)-smodelt(i)
          dmin = dmin + b*b
        end do
        mopts = 1
        do j=2,ntot
           d = 0
           do i=1,moddim
             b = na_models(i,j)-smodelt(i)
             d = d + b*b
           end do
           if(d.lt.dmin)then
             dmin = d
             mopts = j
           end if
        end do

        return
        end
