c                                               and initialize current point
c                                               (used by NA_sample) to
c                                               mid-point of parameter space

        do i=1,nd
           x(i) = (ranget(2,i)+ranget(1,i))/2.0
c          x(i) = 0.0
        end do
        
c						write out information
c						on parameter space
c						to summary file
