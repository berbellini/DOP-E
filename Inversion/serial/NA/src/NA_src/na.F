c ----------------------------------------------------------------------------
c
c       jumble - randomly re-arranges input array  
c
c       Calls ran3 and assumes that it has been initialized.
c
c                                               M. Sambridge, Oct. 1999.
c
c ----------------------------------------------------------------------------
c
        Subroutine jumble(iarr,arr,n)
        implicit none 
        integer         n
        integer         iarr(n)
        real*4          arr(n)
        integer         iseed
        real*4          sobol 
        common/randomtype/sobol,iseed

        real*4 rn,val,ran3
        integer j,k,ival

        rn = n
        do j=1,n
           val = ran3(iseed)
           k = 1 + int(val*rn)
           if(k.eq.n+1)then
              k = n
           else if(k.gt.n)then
              write(*,*)' error in jumble k'
     &        ,k,' val',val,' rn',rn
              call abort
           end if
           ival = iarr(j)
           iarr(j) = iarr(k)
           iarr(k) = ival
           val = arr(j)
           arr(j) = arr(k)
           arr(k) = val
        end do


        return
        end
